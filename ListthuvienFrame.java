    private void xoaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoaButtonActionPerformed
        // TODO add your handling code here:
        int row = sachTable.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(ListthuvienFrame.this, "vui long chon du lieu truoc","Loi",JOptionPane.ERROR_MESSAGE);
        }else{
        int confirm = JOptionPane.showConfirmDialog(ListthuvienFrame.this, "ban chac chan muon xoa khong?");
        if(confirm==JOptionPane.YES_OPTION){
            int sachid = Integer.valueOf(String.valueOf(sachTable.getValueAt(row, 0)));
            
            try {
                thuvienservice.xoadulieu(sachid);
            } catch (SQLException ex) {
                Logger.getLogger(ListthuvienFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        defaultTableModel.setRowCount(0);
        List<sach> items = null;
        try {
            items = thuvienservice.getALLItems1();
        } catch (SQLException ex) {
            Logger.getLogger(ListthuvienFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        for(sach item2 : items ){
        defaultTableModel.addRow(new Object[]{item2.getIDtailieu(),item2.getTennxs(),item2.getTentacgia(), item2.getSotrang(),item2.getSobanphathanh()});     
        }               
        }
        }
    }//GEN-LAST:event_xoaButtonActionPerformed

  
