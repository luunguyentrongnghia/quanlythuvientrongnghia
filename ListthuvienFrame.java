   public void fillTable1() throws SQLException{
    DefaultTableModel tbModel = (DefaultTableModel) sachTable.getModel();
    tbModel.setRowCount(0);
    for(sach Sach : dao.timkiemsach(strfind) ){
       Object dataRow[] = new Object[5];
       dataRow[0]=Sach.getIDtailieu();
       dataRow[1]=Sach.getTennxs();
       dataRow[2]=Sach.getLoai();
       dataRow[3]=Sach.getTentacgia();
       dataRow[4]=Sach.getSotrang();
       tbModel.addRow(dataRow);
    
    }
   
   
   }
    
